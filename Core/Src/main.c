void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)/*捕获脉冲时发生中断*/
{
	if ((TIM2CH1_CAPTURE_STA&0X80)==0)       /*还未成功捕获*/
	{
		if(TIM2CH1_CAPTURE_STA&0X40)         /*捕获到第二个上升沿代表捕获到一个完整的脉冲*/
		{
			TIM2CH1_CAPTURE_STA|=0X80;
			TIM2CH1_CAPTURE_VAL=HAL_TIM_ReadCapturedValue(&htim2,TIM_CHANNEL_1)+1;   /*获取当前的捕获值*/
			TIM_RESET_CAPTUREPOLARITY(&htim2,TIM_CHANNEL_1);                     /*清楚捕获到的值8*/
		}
		else
		{
			TIM2CH1_CAPTURE_STA=0;                            /*清空*/
			TIM2CH1_CAPTURE_VAL=0;
			TIM2CH1_CAPTURE_STA|=0x40;                        /*标记捕获到了上升沿*/
			__HAL_TIM_DISABLE(&htim2);                        /*关闭定时器2*/
			__HAL_TIM_SetCounter(&htim2,0);
			TIM_RESET_CAPTUREPOLARITY(&htim2,TIM_CHANNEL_1);
			__HAL_TIM_ENABLE(&htim2);                         /*使能定时器2*/
		}
	}

}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	
	if((TIM2CH1_CAPTURE_STA&0X80)==0)           /*还未成功捕获*/
	{
		if(TIM2CH1_CAPTURE_STA&0X40)             /*已经捕获到高电平了*/
		{
			if((TIM2CH1_CAPTURE_STA&0X3F)==0X3F)   /*高电平时间太长*/
			{
				TIM2CH1_CAPTURE_STA|=0X80;           /*标记成功捕获一次*/
				TIM2CH1_CAPTURE_VAL=0XFFFFFFFF;
			}
			else
			{
				TIM2CH1_CAPTURE_STA++;
			}
		}
	}
	
}

